#!/usr/bin/env python3
a = {3, 14, 15, 9, 26, 5, 35, 9}
b = {60, 22, 14, 0, 9}
print(a, "\n", b)
print("A diferença entre o conteúdo das duas variáveis é: ", a - b)             #A diferença entre dois sets se dá pelo operador " - "
print("A união entre o conteúdo das duas variáveis é: ", a | b)                 #A união entre dois sets se dá pelo operador " | "
print("A intersecção entre o conteúdo das duas variáveis é: ", a & b)           #A intesecção entre dois sets se dá pelo operador " & "
print("A diferença simétrica entre o conteúdo das duas variáveis é: ", a ^ b)   #A diferença simétrica entre dois sets se da pelo operador " ^ "

